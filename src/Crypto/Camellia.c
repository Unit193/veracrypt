	// r = 17
	lp = l;
	l = r ^ F(l, ks[22]);

	// r = 18
	r = l;
	l = lp ^ F(l, ks[23]);
	l = FL(l, ks[24]);
	r = FL1(r, ks[25]);

	// r = 19
	lp = l;
	l = r ^ F(l, ks[26]);

	// r = 20
	r = l;
	l = lp ^ F(l, ks[27]);

	// r = 21
	lp = l;
	l = r ^ F(l, ks[28]);

