	r = FL1(r, ks[17]);

	// r = 13
	lp = l;
	l = r ^ F(l, ks[18]);

	// r = 14
	r = l;
	l = lp ^ F(l, ks[19]);

	// r = 15
	lp = l;
	l = r ^ F(l, ks[20]);

	// r = 16
